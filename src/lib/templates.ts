/**
 * Project templates for common Portuguese building types.
 * Pre-fills BuildingProject fields so users start from a realistic baseline
 * instead of filling 200+ fields from scratch.
 */

import type { BuildingProject } from "./types";
import { DEFAULT_PROJECT } from "./defaults";

export interface ProjectTemplate {
  id: string;
  nameKey: string; // i18n key
  descriptionKey: string; // i18n key
  icon: string; // emoji
  category: "residential" | "commercial" | "mixed";
  project: BuildingProject;
}

function template(
  id: string,
  nameKey: string,
  descriptionKey: string,
  icon: string,
  category: ProjectTemplate["category"],
  overrides: Partial<BuildingProject> & { [K in keyof BuildingProject]?: Partial<BuildingProject[K]> extends object ? Partial<BuildingProject[K]> : BuildingProject[K] },
): ProjectTemplate {
  // Deep merge overrides into DEFAULT_PROJECT
  const project: BuildingProject = {
    ...DEFAULT_PROJECT,
    ...overrides,
    location: { ...DEFAULT_PROJECT.location, ...(overrides.location ?? {}) },
    architecture: { ...DEFAULT_PROJECT.architecture, ...(overrides.architecture ?? {}) },
    structural: { ...DEFAULT_PROJECT.structural, ...(overrides.structural ?? {}) },
    fireSafety: { ...DEFAULT_PROJECT.fireSafety, ...(overrides.fireSafety ?? {}) },
    avac: { ...DEFAULT_PROJECT.avac, ...(overrides.avac ?? {}) },
    waterDrainage: { ...DEFAULT_PROJECT.waterDrainage, ...(overrides.waterDrainage ?? {}) },
    gas: { ...DEFAULT_PROJECT.gas, ...(overrides.gas ?? {}) },
    electrical: { ...DEFAULT_PROJECT.electrical, ...(overrides.electrical ?? {}) },
    telecommunications: { ...DEFAULT_PROJECT.telecommunications, ...(overrides.telecommunications ?? {}) },
    envelope: { ...DEFAULT_PROJECT.envelope, ...(overrides.envelope ?? {}) },
    systems: { ...DEFAULT_PROJECT.systems, ...(overrides.systems ?? {}) },
    acoustic: { ...DEFAULT_PROJECT.acoustic, ...(overrides.acoustic ?? {}) },
    accessibility: { ...DEFAULT_PROJECT.accessibility, ...(overrides.accessibility ?? {}) },
    elevators: { ...DEFAULT_PROJECT.elevators, ...(overrides.elevators ?? {}) },
    licensing: { ...DEFAULT_PROJECT.licensing, ...(overrides.licensing ?? {}) },
    waste: { ...DEFAULT_PROJECT.waste, ...(overrides.waste ?? {}) },
    localRegulations: { ...DEFAULT_PROJECT.localRegulations, ...(overrides.localRegulations ?? {}) },
    drawingQuality: { ...DEFAULT_PROJECT.drawingQuality, ...(overrides.drawingQuality ?? {}) },
    projectContext: { ...DEFAULT_PROJECT.projectContext, ...(overrides.projectContext ?? {}) },
  } as BuildingProject;
  return { id, nameKey, descriptionKey, icon, category, project };
}

